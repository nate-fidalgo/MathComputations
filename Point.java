
public class Point {

private int x_coordinate ;
private int y_coordinate ;


public Point( int x , int y )
{

    x_coordinate = x ;
    y_coordinate = y ;

}



public void set_x( int x )
{

  x_coordinate = x ;

}



public void set_y( int y )
{

  y_coordinate = y ;

}



public int get_x()
{

  return x_coordinate ;

}


public int get_y()
{

  return y_coordinate ;

}







}
